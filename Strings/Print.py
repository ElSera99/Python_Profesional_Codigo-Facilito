#Mejoras en el uso de la función print

nombre = 'Serafin'
apellido = 'Tierrafria'

#Uso de concatenado en función print
# Al separar por comas los valores serán separados por espacios a la salida
# El parámetro 'sep' nos ayuda a nombrar el caracter que separa a la salida del print, por defecto es un espacio
print(nombre, apellido, 'Báez', sep = '-')